  function initSwipe(){
    const isTouch = ('ontouchstart' in window) || (navigator.maxTouchPoints>0);
    if(!isTouch) return; // Îç∞Ïä§ÌÅ¨ÌÜ± ÎπÑÌôúÏÑ±Ìôî

    let startX = 0;
    let endX = 0;
    let swiping = false;
    const threshold = 50; // ÏùºÎ∞òÏ†ÅÏù∏ Í∞êÎèÑ

    document.addEventListener('touchstart', (e)=>{
      swiping = true;
      startX = e.touches[0].clientX;
    }, {passive:true});

    document.addEventListener('touchmove', (e)=>{
      if(!swiping) return;
      endX = e.touches[0].clientX;
    }, {passive:true});

    document.addEventListener('touchend', ()=>{
      if(!swiping) return;
      const dx = endX - startX;
      if(Math.abs(dx) > threshold){
        const tabs = Array.from(document.querySelectorAll('.tab-btn'));
        const activeIdx = tabs.findIndex(b=>b.classList.contains('border-blue-600'));
        if(dx < 0){
          // ÏôºÏ™ΩÏúºÎ°ú Ïä§ÏôÄÏù¥ÌîÑ ‚Üí Îã§Ïùå ÌÉ≠ (ÎßàÏßÄÎßâ ÌÉ≠ÏóêÏÑú Î©àÏ∂§)
          if(activeIdx < tabs.length-1){ tabs[activeIdx+1].click(); }
        }else{
          // Ïò§Î•∏Ï™ΩÏúºÎ°ú Ïä§ÏôÄÏù¥ÌîÑ ‚Üí Ïù¥Ï†Ñ ÌÉ≠ (Ï≤´ ÌÉ≠ÏóêÏÑú Î©àÏ∂§)
          if(activeIdx > 0){ tabs[activeIdx-1].click(); }
        }
      }
      swiping = false;
      startX = endX = 0;
    });
  }
